# -- Compile an individual TypeScript script to JavaScript --

# Install typescript as a project dependency
npm install typescript

# Using TypeScript compiler (tsc)
npx tsc script.ts

# -- Compile a TypeScript project (with `tsconfig.json`) to JavaScript --

# Install typescript as a project dependency
npm install typescript

# Using TypeScript compiler (tsc)
npx tsc

# -- Compile a TypeScript script to JavaScript and run the script --

# Install tsx as a project dependency
npm install tsx

# Using TypeScript Execute (tsx)
npx tsx script.ts
